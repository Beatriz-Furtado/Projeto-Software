package src;

public class Musica {
    public void play() {
        System.out.println("Música tocando.");
    }

    public void stop() {
        System.out.println("Música parada");
    }

    public void goBack() {
        System.out.println("Voltar Música");
    }

    public void goForward() {
        System.out.println("Pular Música");
    }
}